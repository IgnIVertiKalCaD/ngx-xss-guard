#!/bin/bash

# Проверяем, существует ли папка src
if [ -d $1 ]; then
  echo "Содержимое файлов в папке 'src' (включая вложенные папки):"
  echo "----------------------------------------------------"
  # Используем find для поиска всех файлов в папке src и ее поддиректориях
  # -type f гарантирует, что мы работаем только с файлами
  # -print0 используется с xargs -0 для безопасной обработки имен файлов с пробелами или спецсимволами
  find $1 -type f -print0 | while IFS= read -r -d $'\0' file; do
    echo "--- Содержимое файла: ${file#$1/} ---" # Выводим путь относительно папки src
    cat "$file"
    echo "" # Пустая строка для разделения содержимого файлов
  done
else
  echo "Ошибка: Папка 'src' не найдена."
  echo "Пожалуйста, убедитесь, что вы запускаете скрипт в той же директории, где находится папка 'src', или укажите правильный путь."
fi
